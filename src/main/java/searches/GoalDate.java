package searches;


public class GoalDate {
	
	// --- Instance variables ----------------------------------------------------------------
	
	private int year;
	private int month;
	private int week;
	private int day;
	
	// --- Getters ---------------------------------------------------------------------------
	
	public int getYear() {
		return year;
	}
	
	public int getMonth() {
		return month;
	}
	
	public int getWeek() {
		return week;
	}
	
	public int getDay() {
		return day;
	}

	// --- Setters ---------------------------------------------------------------------------
	
	public void setYear(int year) {
		this.year = year;
	}

	public void setMonth(int month) {
		this.month = month;
	}

	public void setWeek(int week) {
		this.week = week;
	}

	public void setDay(int day) {
		this.day = day;
	}
	
}
